package com.clarity.one.model.geocode;

public class Location {

	private double lat;
	private double lng;
	
	public double getLat(){
		return lat;
	}
	
	public void setLat(double l){
		this.lat = l;
	}
	
	public double getLng(){
		return lng;
	}
	
	public void setLng(double l){
		this.lng = l;
	}
	
}
